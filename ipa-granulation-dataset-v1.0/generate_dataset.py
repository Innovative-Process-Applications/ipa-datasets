"""
IPA Dry Granulation: Multi-Material Roll Compaction & Milling Dataset Generator v1.0
=====================================================================================
Generates a physically plausible synthetic dataset grounded in the granulation
fundamentals described in:

  Szappanos-Csordás, K. (2018). "Impact of material properties, process
  parameters and roll compactor design on roll compaction." Doctoral dissertation,
  Heinrich-Heine-Universität Düsseldorf.

Key concepts from Chapter 1 modeled here:
  - Three material deformation classes: brittle (mannitol), plastic (MCC 101),
    and a 1:1 blend (mixture) — as used in the dissertation's experimental design
  - Densification factor (DF = drawn-in width / gap width) scales with roll
    diameter (Eq. 1–3 in the dissertation)
  - Nip angle dependency on roll surface (smooth < knurled) and material friction
  - Roll speed sensitivity: plastic materials are dwell-time sensitive; brittle
    materials are insensitive (Section 1.2)
  - Sealing system effect: rim-rolls produce more uniform ribbons than side-sealing
    (Section 1.2)
  - Ribbon relative density target window: 0.60–0.80 for downstream tabletability
    (Zinchuk criterion, Section 1.3)
  - Fines fraction dependency on compaction force and sealing system

Machine design parameters are drawn from Table 1 of the dissertation, which
catalogs real laboratory-scale roll compactors from multiple manufacturers.

THIS IS SYNTHETIC EDUCATIONAL DATA. NOT REAL CUSTOMER OR LAB DATA.
Generated by Innovative Process Applications (IPA) for teaching DOE/RSM/QbD.
"""

import numpy as np
import pandas as pd

rng = np.random.default_rng(seed=2026)

N_RUNS = 720  # 3 materials × 2 sealing systems × many parameter combos

# =============================================================================
# MATERIAL PROPERTIES
# =============================================================================
# Grounded in Chapter 1 and the dissertation's experimental materials
MATERIALS = {
    "MCC_101": {
        "label": "Microcrystalline Cellulose (MCC 101)",
        "true_density_gcc": 1.55,       # helium pycnometry, literature
        "bulk_density_gcc": 0.32,
        "deformation": "plastic",       # dwell-time sensitive
        "heckel_k": 0.020,              # high compressibility
        "heckel_a": 0.55,
        "friction_coeff": 0.45,         # moderate wall friction
        "fragmentation_rate": 0.15,     # low — deforms rather than fragments
        "rs_sensitivity": 0.85,         # high sensitivity to roll speed
    },
    "Mannitol_SD": {
        "label": "Spray-Dried Mannitol",
        "true_density_gcc": 1.49,
        "bulk_density_gcc": 0.48,
        "deformation": "brittle",       # fragments under pressure
        "heckel_k": 0.012,              # lower compressibility
        "heckel_a": 0.48,
        "friction_coeff": 0.35,
        "fragmentation_rate": 0.70,     # high — brittle fracture
        "rs_sensitivity": 0.15,         # insensitive to roll speed
    },
    "MCC_Mannitol_Mix": {
        "label": "1:1 MCC 101 / Mannitol Mixture",
        "true_density_gcc": 1.52,       # weighted average
        "bulk_density_gcc": 0.38,
        "deformation": "mixed",
        "heckel_k": 0.016,
        "heckel_a": 0.52,
        "friction_coeff": 0.40,
        "fragmentation_rate": 0.40,
        "rs_sensitivity": 0.50,
    },
}

# =============================================================================
# MACHINE DESIGN CONFIGURATIONS
# =============================================================================
# Based on Table 1 in the dissertation (real compactor specifications)
COMPACTOR_CONFIGS = [
    {"roll_diameter_mm": 120, "roll_width_mm": 25, "label": "Small_D120"},
    {"roll_diameter_mm": 250, "roll_width_mm": 25, "label": "Medium_D250"},
    {"roll_diameter_mm": 250, "roll_width_mm": 30, "label": "Medium_D250_W30"},
]

SEALING_SYSTEMS = ["side_sealing", "rim_rolls"]
ROLL_SURFACES = ["smooth", "knurled"]

# =============================================================================
# PROCESS PARAMETER RANGES
# =============================================================================
SCF_RANGE = (2.0, 20.0)        # kN/cm — spans Table 1 range
GAP_WIDTH_RANGE = (1.0, 6.0)   # mm
ROLL_SPEED_RANGE = (1.0, 30.0) # rpm — full range from Table 1
FEED_SPEED_RANGE = (10.0, 120.0)

# =============================================================================
# PHYSICAL MODEL FUNCTIONS
# =============================================================================

def densification_factor(roll_diameter_mm, gap_width_mm):
    """
    DF = DW/GW increases with roll diameter (Eq. 1–3 in dissertation).
    Larger rolls → wider drawn-in width → higher densification at same gap.
    """
    # Empirical: DF scales with sqrt(D/GW), capped at physical limits
    df = 1.0 + 1.8 * np.sqrt(roll_diameter_mm / (gap_width_mm * 100))
    return np.clip(df, 1.5, 6.0)

def nip_angle_deg(roll_surface, friction_coeff, feed_speed, roll_speed):
    """
    Nip angle depends on roll surface roughness and material friction.
    Knurled > smooth. Higher pre-densification (feed/roll ratio) increases
    nip angle. (Section 1.2 of the dissertation.)
    """
    base = 8.0 if roll_surface == "smooth" else 13.0  # degrees
    feed_ratio = feed_speed / np.clip(roll_speed, 0.5, None)
    ratio_effect = 1.5 * np.log1p(feed_ratio / 10.0)
    friction_effect = 5.0 * friction_coeff
    return base + ratio_effect + friction_effect

def peak_nip_pressure_mpa(scf_kn_per_cm, gap_mm, roll_diameter_mm, nip_angle):
    """
    Approximate peak nip pressure. Scales with SCF and inversely with
    contact area (which depends on roll geometry and nip angle).
    """
    contact_length_mm = (roll_diameter_mm / 2) * np.radians(nip_angle)
    pressure = (scf_kn_per_cm * 100.0) / np.clip(contact_length_mm, 5.0, None)
    return pressure

def ribbon_relative_density(pressure_mpa, heckel_k, heckel_a, df,
                            roll_speed, rs_sensitivity,
                            sealing_system, roll_surface):
    """
    Heckel densification modified by:
    - Densification factor (machine geometry effect)
    - Roll speed / dwell time (material-dependent sensitivity)
    - Sealing system (rim-rolls retain more material in the nip)
    """
    # Base Heckel: D = 1 - exp(-(K*P + A))
    base_rd = 1.0 - np.exp(-(heckel_k * pressure_mpa + heckel_a))

    # DF effect: higher DF → more pre-densification → slight boost
    df_factor = 1.0 + 0.03 * (df - 2.0)

    # Roll speed / dwell time effect (Section 1.2):
    # For plastic materials (high rs_sensitivity), fast RS reduces density.
    # For brittle materials (low rs_sensitivity), RS has negligible effect.
    # Dwell time inversely proportional to RS
    dwell_penalty = 1.0 - rs_sensitivity * 0.008 * np.clip(roll_speed - 5.0, 0, None)

    # Sealing: rim-rolls retain powder better → +2-3% relative density
    seal_bonus = 1.02 if sealing_system == "rim_rolls" else 1.00

    # Roll surface: knurled → better grip → slightly higher density
    surf_bonus = 1.01 if roll_surface == "knurled" else 1.00

    rd = base_rd * df_factor * dwell_penalty * seal_bonus * surf_bonus
    return np.clip(rd, 0.35, 0.95)

def ribbon_density_cv(sealing_system, roll_surface, feed_speed, roll_speed,
                      friction_coeff):
    """
    Across-width density uniformity (CV%).
    Rim-rolls >> side-sealing for uniformity (Section 1.2).
    Twin feed screw at optimal ratio improves uniformity.
    """
    base_cv = 3.0 if sealing_system == "rim_rolls" else 6.0
    # Feed ratio optimality
    ratio = feed_speed / np.clip(roll_speed, 0.5, None)
    ratio_penalty = 2.0 * np.abs(ratio - 11.0) / 11.0
    # Knurled improves grip → less edge loss → lower CV
    surf_reduction = -0.8 if roll_surface == "knurled" else 0.0
    cv = base_cv + ratio_penalty + surf_reduction
    return np.clip(cv, 1.0, 15.0)

def fines_fraction(rd, sealing_system, fragmentation_rate):
    """
    Fines fraction (% of granules < 100 µm after milling).
    Lower ribbon density → weaker ribbons → more fines.
    Rim-rolls reduce fines. Brittle materials fragment more.
    (Section 1.2: "high amount of uncompacted material")
    """
    # Base: inverse relationship with ribbon density
    base_fines = 60.0 * (1.0 - rd)
    # Sealing effect: SS produces more bypass/fines
    seal_effect = 5.0 if sealing_system == "side_sealing" else 0.0
    # Material fragmentation: brittle materials produce more fines on milling
    frag_effect = 8.0 * fragmentation_rate
    fines = base_fines + seal_effect + frag_effect
    return np.clip(fines, 5.0, 65.0)

def granule_d50_um(rd, fragmentation_rate, roll_speed):
    """
    Median granule size (d50) after oscillating mill.
    Higher ribbon density → larger granules.
    Brittle materials → smaller granules (more fragmentation).
    """
    base_d50 = 200.0 + 800.0 * (rd - 0.50)
    frag_effect = -150.0 * fragmentation_rate
    # Roll speed: faster RS → slightly smaller granules for plastic materials
    rs_effect = -2.0 * (roll_speed - 5.0)
    d50 = base_d50 + frag_effect + rs_effect
    return np.clip(d50, 80.0, 900.0)

def throughput_kg_hr(roll_speed, gap_mm, roll_width_mm, ribbon_density_gcc):
    """Simple mass throughput estimate."""
    return (roll_speed * gap_mm * roll_width_mm * ribbon_density_gcc * 60
            / 1000 * 0.85)

# =============================================================================
# GENERATE RUNS
# =============================================================================
rows = []
run_id = 0

for _ in range(N_RUNS):
    # Random selections
    mat_key = rng.choice(list(MATERIALS.keys()))
    mat = MATERIALS[mat_key]
    compactor = COMPACTOR_CONFIGS[rng.integers(len(COMPACTOR_CONFIGS))]
    seal = rng.choice(SEALING_SYSTEMS)
    surf = rng.choice(ROLL_SURFACES)

    # Random process parameters
    scf = rng.uniform(*SCF_RANGE)
    gw = rng.uniform(*GAP_WIDTH_RANGE)
    rs = rng.uniform(*ROLL_SPEED_RANGE)
    fs = rng.uniform(*FEED_SPEED_RANGE)

    # Physics chain
    df = densification_factor(compactor["roll_diameter_mm"], gw)
    nip = nip_angle_deg(surf, mat["friction_coeff"], fs, rs)
    pressure = peak_nip_pressure_mpa(scf, gw, compactor["roll_diameter_mm"], nip)

    rd = ribbon_relative_density(
        pressure, mat["heckel_k"], mat["heckel_a"], df,
        rs, mat["rs_sensitivity"], seal, surf
    )
    # Add measurement noise
    rd += rng.normal(0, 0.012)
    rd = np.clip(rd, 0.35, 0.95)

    rib_density = rd * mat["true_density_gcc"]
    porosity = 1.0 - rd

    cv = ribbon_density_cv(seal, surf, fs, rs, mat["friction_coeff"])
    cv += rng.normal(0, 0.5)
    cv = np.clip(cv, 1.0, 15.0)

    fines = fines_fraction(rd, seal, mat["fragmentation_rate"])
    fines += rng.normal(0, 2.0)
    fines = np.clip(fines, 3.0, 70.0)

    d50 = granule_d50_um(rd, mat["fragmentation_rate"], rs)
    d50 += rng.normal(0, 25.0)
    d50 = np.clip(d50, 50.0, 1000.0)

    tp = throughput_kg_hr(rs, gw, compactor["roll_width_mm"], rib_density)
    tp += rng.normal(0, 1.5)
    tp = np.clip(tp, 0, None)

    # Zinchuk criterion: is ribbon in the tabletable window?
    in_zinchuk_window = "Yes" if 0.60 <= rd <= 0.80 else "No"

    run_id += 1
    rows.append({
        "run_id": run_id,
        # Material
        "material": mat_key,
        "deformation_type": mat["deformation"],
        "true_density_gcc": mat["true_density_gcc"],
        # Machine design
        "compactor_config": compactor["label"],
        "roll_diameter_mm": compactor["roll_diameter_mm"],
        "roll_width_mm": compactor["roll_width_mm"],
        "sealing_system": seal,
        "roll_surface": surf,
        # Process parameters
        "scf_kN_per_cm": round(scf, 2),
        "gap_width_mm": round(gw, 2),
        "roll_speed_rpm": round(rs, 2),
        "feed_screw_rpm": round(fs, 1),
        # Intermediate calculations
        "densification_factor": round(df, 3),
        "nip_angle_deg": round(nip, 2),
        "peak_pressure_MPa": round(pressure, 1),
        # Ribbon quality attributes
        "ribbon_rel_density": round(rd, 4),
        "ribbon_density_gcc": round(rib_density, 4),
        "ribbon_porosity": round(porosity, 4),
        "density_CV_percent": round(cv, 2),
        "in_zinchuk_window": in_zinchuk_window,
        # Granule quality attributes
        "fines_fraction_pct": round(fines, 2),
        "granule_d50_um": round(d50, 1),
        # Throughput
        "throughput_kg_hr": round(tp, 2),
    })

df_out = pd.DataFrame(rows)
df_out.to_csv("dry_granulation_multi_factor_v1.0.csv", index=False)

print(f"Wrote {len(df_out)} rows, {len(df_out.columns)} columns")
print()
print("=== Material distribution ===")
print(df_out["material"].value_counts())
print()
print("=== Sealing system distribution ===")
print(df_out["sealing_system"].value_counts())
print()
print("=== Zinchuk window compliance ===")
print(df_out["in_zinchuk_window"].value_counts())
print()
print("=== Summary statistics ===")
print(df_out.describe().round(3))
